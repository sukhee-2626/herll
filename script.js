// FEATURE CARD EXPAND/COLLAPSE
// ===================================
function toggleDetails(button) {
    const card = button.closest('.feature-card');
    const details = card.querySelector('.feature-details');

    if (details.classList.contains('active')) {
        details.classList.remove('active');
        button.textContent = 'Show me more';
    } else {
        // Close all other open details
        document.querySelectorAll('.feature-details.active').forEach(detail => {
            detail.classList.remove('active');
            const btn = detail.closest('.feature-card').querySelector('.show-more-btn');
            if (btn) btn.textContent = 'Show me more';
        });

        details.classList.add('active');
        button.textContent = 'Show less';
    }
}

